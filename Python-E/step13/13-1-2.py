if '5' in '12345':  #文字列
    print('5を含む')

if 5 in [1,2,3,4,5]:#リスト
    print('5を含む')

if 5 in (1,2,3,4,5):#タプル
    print('5を含む')

if 5 in {1,2,3,4,5}:#集合
    print('5を含む')

if 5 in {5:'ご'}:#辞書
    print('5を含む')

if 5 in {'ご':5}:#辞書
    print('5を含む')

